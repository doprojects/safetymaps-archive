            <h2>Resources for emergency and disasters</h2>
            <p>
                We’re grateful for your interest in Safety Maps, but also want
                you to make sure you know about the many excellent initiatives
                around the world that are specifically dedicated to emergency
                preparedness and disaster recovery.
            </p>

            <p>
                Here are a few representative organizations and civil
                activities/efforts that you might find helpful. If you’re aware
                of one that isn’t listed here, feel free to <a href="mailto:nk@doprojects.org">suggest it to us</a>
                for inclusion.
            </p>
            <h2>General resources</h2>
            <ul>
                   <li><a href="http://www.ncdc.noaa.gov/img/reports/billion/billion2010.pdf">Billion-Dollar Weather Disasters, 1980-2010</a>.</li>
                   <li><a href="http://www.google.com/crisisresponse/">Google Crisis Response</a>.</li>
                   <li><a href="http://hisz.rsoe.hu/alertmap/">Hungarian National Association of Radio Distress-Signalling and Infocommunications (RSOE)</a>. (Disregard the arcane name; this is a very useful global map of humanitarian incidents.)</li>
                   <li><a href="http://www.reliefweb.int/rw/rwb.nsf/doc114?OpenForm">United Nations site with information on humanitarian relief organizations</a>.</li>
            </ul>

             <h2>By disaster type</h2>

             <h3>Earthquakes</h3>
             <ul>
                   <li><a href="http://www.seismo.ethz.ch/static/gshap/">Global Seismic Hazard Assessment Program</a>.</li>
                   <li><a href="http://gldims.cr.usgs.gov/">US National Seismic Hazard Maps</a>.</li>
             </ul>

             <h3>Tornadoes</h3>
             <ul>
                   <li><a href="http://www.tornadohistoryproject.com/">US tornadoes,1950-2009</a>. Over 53,000 tornado maps.</li>
                   <li><a href="http://www.britannica.com/EBchecked/media/19250/Map-of-worldwide-tornado-intensity-and-occurrence-As-can-be#">Tornado: Worldwide Tornado Intensity Map</a>.</li>
             </ul>

             <h3>Volcanoes</h3>
             <ul>
                   <li><a href="http://www.volcano.si.edu/world/region.cfm?rnum=07">Volcano map</a>.</li>
             </ul>

             <h3>Emergency Preparedness and Response</h3>
              <ul>
                   <li><a href="http://emergency.cdc.gov/preparedness/">US Centers for Disease Control emergency preparedness</a>.</li>
                   <li><a href="http://www.redcross.org/portal/site/en/menuitem.86f46a12f382290517a8f210b80f78a0/?vgnextoid=92d51a53f1c37110VgnVCM1000003481a10aRCRD">International Red Cross Emergency-specific preparedness information</a>.</li>
              </ul>

              <h2>By city</h2>

              <h3>San Francisco</h3>
              <ul>
                <li><a href="http://www.sf-fire.org/index.aspx?page=859">San Francisco Fire Department Neighborhood Emergency Response Team</a> (NERT), a community based training program dedicated to a neighbor-helping-neighbor approach.</li>
                <li><a href="http://72hours.org/">72Hours.org</a>, an effort of the City and County of San Francisco. Are you prepared? </li>
                <li><a href="http://www.sfdem.org/">San Francisco Department of Emergency Management</a>.</li>
                <li>SPUR’s <a href="http://www.spur.org/policy/the-resilient-city">Resilient City</a> project.</li>
            </ul>

            <h3>New York</h3>
            <ul>
                <li>New York City <a href="http://www.nyc.gov/html/oem/html/home/home.shtml">Office of Emergency Management</a> and <a href="http://www.nyc.gov/html/oem/html/get_prepared/ready.shtml">Get Prepared</a>.</li>
                <li>New York State <a href="http://www.semo.state.ny.us/">Office of Emergency Management</a>.</li>
                <li>Community Emergency Response Team (CERT) <a href="http://www.nyc.gov/html/oem/html/get_prepared/cert.shtml">program information</a>.</li>
                <li><a href="http://www.brooklynsouthcert.com/">Brooklyn South Community Emergency Response Team</a>.</li>
            </ul>

